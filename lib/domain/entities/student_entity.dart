class Student {
  final String id;
  final String name;
  final String cpf;
  final String email;
  final String academicRecord;
  final DateTime createdAt;
  final DateTime birthdate;

  Student({
    required this.id,
    required this.name,
    required this.cpf,
    required this.email,
    required this.academicRecord,
    required this.createdAt,
    required this.birthdate,
  });
}
